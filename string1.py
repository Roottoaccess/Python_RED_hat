name = "Java Coding"

print(name)

# Type of the variable
print(type(name))

# String are immutable

# Length of the string
print(len(name))

# Slicing of the string
nameshort = name[0:3] # Start from index 0 all the way till 3(excluding 3)


print("After slicing the string: ",nameshort)

# Negative slicing in the python
pet = "Tiger"

print(pet)

# Negative slicing
print(pet[-4: -1])
# Converting the negative index into corresponding positive index
print(pet[1: 4])


# Slicing with skip value
word =  "012345678"
print(word[1: 6: 2])

