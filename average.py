# WRITING A PYTHON SCRIPT TO FIND THE AVERAGE OF 2 NUMBERS
# Taking the user input of the 2 numbers
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the secound number: "))

# Calculating the average of 2 numbers
avg = (num1 + num2) / 2

# Printing the final result
print("The average of the 2 numbers are: ",avg)